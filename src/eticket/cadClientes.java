    public void consultaCliente() throws Exception{
        String cpf = jTextFieldCpf.getText();
        Statement stm = Conexao.conectar();
            ResultSet rs = stm.executeQuery("SELECT * FROM CLIENTES WHERE CLIENTES.CPF = '"+cpf+"';");
            while(rs.next()){
                String cpfCliente = rs.getString("CPF");
                    if(cpf.equals(cpfCliente)){
                        jTextFieldNome.setText(rs.getString("NOME"));
                        jTextFieldEndereco.setText(rs.getString("ENDERECO"));
                        jTextFieldBairro.setText(rs.getString("BAIRRO"));
                        jTextFieldCidade.setText(rs.getString("CIDADE"));
                        jTextFieldCep.setText(rs.getString("CEP"));
                        jTextFieldEmail.setText(rs.getString("EMAIL"));
                        jTextFieldRG.setText(rs.getString("RG"));
                        jTextFieldTelefone.setText(rs.getString("TELEFONE"));
                        i = 1;
                    }
                        
                }
    }
     
