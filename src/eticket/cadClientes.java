    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try{
           
         if(i!=0){
            Cliente cliente = new Cliente();
            cliente.setNome(jTextFieldNome.getText());
            cliente.setCpf(jTextFieldCpf.getText());
            cliente.setEndereco(jTextFieldEndereco.getText());
            cliente.setCidade(jTextFieldCidade.getText());
            cliente.setBairro(jTextFieldBairro.getText());
            cliente.setEmail(jTextFieldEmail.getText());
            cliente.setRg(jTextFieldRG.getText());
            cliente.setCep(jTextFieldCep.getText());
            cliente.setTelefone(jTextFieldTelefone.getText());
            cliente.setUf(jComboBoxUf.getSelectedItem()+"");
            RepCliente.obterInstancia().atualizar(cliente);
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso");
            jButtonLimparActionPerformed(evt);
         }else{
             Cliente cliente = new Cliente();
            cliente.setNome(jTextFieldNome.getText());
            cliente.setCpf(jTextFieldCpf.getText());
            cliente.setEndereco(jTextFieldEndereco.getText());
            cliente.setCidade(jTextFieldCidade.getText());
            cliente.setBairro(jTextFieldBairro.getText());
            cliente.setEmail(jTextFieldEmail.getText());
            cliente.setRg(jTextFieldRG.getText());
            cliente.setCep(jTextFieldCep.getText());
            cliente.setTelefone(jTextFieldTelefone.getText());
            cliente.setUf(jComboBoxUf.getSelectedItem()+"");
            RepCliente.obterInstancia().inserir(cliente);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
            jButtonLimparActionPerformed(evt);
         }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

