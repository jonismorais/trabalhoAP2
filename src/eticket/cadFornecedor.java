    public void consultaFornecedor() throws Exception{
        String cnpj = jTextFieldCnpjFornecedor.getText();
        Statement stm = Conexao.conectar();
            ResultSet rs = stm.executeQuery("SELECT * FROM FORNECEDORES WHERE FORNECEDORES.CNPJ = '"+cnpj+"';");
            while(rs.next()){
                String cnpjFornecedor = rs.getString("CNPJ");
                    if(cnpj.equals(cnpjFornecedor)){
                        jTextFieldNome.setText(rs.getString("RAZAOSOCIAL"));
                        jTextFieldEndereco.setText(rs.getString("ENDERECO"));
                        jTextFieldBairro.setText(rs.getString("BAIRRO"));
                        jTextFieldCidade.setText(rs.getString("CIDADE"));
                        jTextFieldCep.setText(rs.getString("CEP"));
                        jTextFieldEmail.setText(rs.getString("EMAIL"));
                        jTextFieldIE.setText(rs.getString("INSCEST"));
                        jTextFieldTelefone.setText(rs.getString("TELEFONE"));
                        i = 1;
                    }
                        
                }
    }
    
    
